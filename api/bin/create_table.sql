DECLARE
sql_stmt long;

BEGIN
    sql_stmt:='
    CREATE TABLE dog(
    id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    breed VARCHAR2(10) NOT NULL,
    color VARCHAR2(10) NOT NULL,
    PRIMARY KEY(id)
)';
    EXECUTE IMMEDIATE sql_stmt;

    EXCEPTION
        WHEN OTHERS THEN
            IF SQLCODE = -955 THEN
                NULL;
            ELSE
                RAISE;
            END IF;
END;
